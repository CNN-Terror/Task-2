class Feature:
  def __init__(self, t, x, y, pressure, penup, azimuth, inclination):
    self.t = t
    self.x = x
    self.y = y
    self.pressure = pressure
    self.penup = penup
    self.azimuth = azimuth
    self.inclination = inclination


  t = None
  x = None
  y = None
  pressure = None
  penup = None
  azimuth = None
  inclination = None
